// 90~100 -> A, 80~89 -> B , 70~79 -> C, 60~69 -> D, X
let score = 60;
if (score >= 90) {
  console.log('A');
} else if (score >= 80) {
  console.log('B');
} else if (score >= 70) {
  console.log('C');
} else if (score >= 60) {
  console.log('D');
} else {
  console.log('X');
}

